dia = int(input("DIGITE UM NUMERO DE 1 A 7: "))

if dia == 1:
    print("DOMINGO")
elif dia == 2:
    print("SEGUNDA")
elif dia == 3:
    print("TERÇA")
elif dia == 4:  
    print("QUARTA")
elif dia == 5:
    print("QUINTA")
elif dia == 6:  
    print("SEXTA")
elif dia == 7:
    print("SÁBADO")


    