#6)Sucessor e antecessor, 
#faça uma aplicação que colete um número digitado pelo usuário e logo em seguida mostre em ordem: 
#o numero anterior a ele, o próprio número escolhido e o número sucessor a ele

num = int(input("DIGITE UM NUMERO: "))

print("ANTECESSOR:", num - 1)
print("NUMERO DIGITADO:", num)
print("SUCESSOR:", num + 1)
